# Как всё работает вместе (упрощённый пример)
# Человек отправляет запрос: «/users/1».
# Контроллер принимает его.
# Контроллер вызывает сервис: «Найди пользователя 1».
# Сервис идёт к репозиторию: «Достань из базы пользователя 1».
# Репозиторий достаёт запись и превращает её в сущность.
# Сервис что-то делает с сущностью (например, проверяет доступ).
# Результат отправляется обратно в контроллер.
# Контроллер превращает сущность в MH_DTO (через маппер) и отправляет ответ пользователю.
